# Operadores aritmeticos

a= int(input("Ingrese un primer numero: "))
b= int(input("Ingrese el segundo numero: "))
print(f'Numeros iniciales: {a}, {b}')
# Suma +
suma= a + b

print(f'Suma: {suma}')

# Resta -
resta = a - b
print(f'Resta: {resta}')

#Multiplicacion *
multitplicacion = a * b
print(f'Multiplicacion: {multitplicacion}')

# Division /
division = a/b
print(f'Division: {division:.2f}')

# Division entera (//)
division_entera = a // b
print(f' Division entera: {division_entera}')

#Modulo % residuo de division
modulo = a % b
print(f'Residuo  de la division: {modulo}')

# Exponente (Poentcia) **

exponente = a ** b
print(f'Exponente: {exponente}')